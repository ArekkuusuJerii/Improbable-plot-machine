package arekkuusu.implom.client.util.sprite;

public final class UVFrame {

	public final double uMin;
	public final double uMax;
	public final double vMin;
	public final double vMax;

	public UVFrame(double uMin, double uMax, double vMin, double vMax) {
		this.uMin = uMin;
		this.uMax = uMax;
		this.vMin = vMin;
		this.vMax = vMax;
	}
}
